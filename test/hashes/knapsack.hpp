template<typename FieldType>
components::blueprint<FieldType> test_knapsack_crh_with_bit_out_component_internal(std::size_t dimension,
                                                       const std::vector<bool> &input_bits,
                                                       const std::vector<bool> &digest_bits) {
    BOOST_CHECK(components::knapsack_dimension<FieldType>::dimension == dimension);
    components::knapsack_crh_with_bit_out_component<FieldType>::sample_randomness(input_bits.size());
    components::blueprint<FieldType> bp;

    components::block_variable<FieldType> input_block(bp, input_bits.size());
    components::digest_variable<FieldType> output_digest(
        bp, components::knapsack_crh_with_bit_out_component<FieldType>::get_digest_len());
    components::knapsack_crh_with_bit_out_component<FieldType> H(bp, input_bits.size(), input_block, output_digest);

    input_block.generate_r1cs_witness(input_bits);
    H.generate_r1cs_constraints();
    H.generate_r1cs_witness();

    BOOST_CHECK(output_digest.get_digest().size() == digest_bits.size());
    BOOST_CHECK(bp.is_satisfied());

    const std::size_t num_constraints = bp.num_constraints();
    const std::size_t expected_constraints =
        components::knapsack_crh_with_bit_out_component<FieldType>::expected_constraints();
    BOOST_CHECK(num_constraints == expected_constraints);

    return bp;
}

